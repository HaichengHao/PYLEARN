# 水仙花数
'''
水仙花数：
举例 153 = 1^3+5^3+3^3

'''
# 题目：输出100-999之间的水仙花数
sum=0
for i in range(100,1000):
    # 个位，除10取余
    g=i%10
    # 十位，除10取整，再除10取余
    s=i//10%10
    # 百位，除100取整
    b=i//100
    if i==pow(g,3)+pow(s,3)+pow(b,s):
        sum+=1
        print(i)
print('100-999内的水仙花数共有'+str(sum)+'个')